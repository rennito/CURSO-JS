// Operadores aritmeticos
let a = 3, b= 2;
let z= a+ b;
console.log("Resultado de la suma: " + z)

z = a - b;
console.log("Resultado de la resta: " + z)

z= a * b
console.log("Resultado de la multiplicacion: " + z)

z= a / b
console.log("Resultado de la divicion: " + z )

z = a % b // residuo de la divisiuon
console.log("Resulto de operacion modulo (residuo):" + z) 

z = a ** b;
console.log("Resultado de operador exponente: " + z)

// incremento y descremento
// preincremento (el operador ++  antes de la varibale)
z= ++a;
console.log(a);
console.log(z);
// post incremtno ( el operador ++ despues de la variable)

z= b++;
console.log(b);
console.log(z);
// decremento
//pre decremento
z= --a;
console.log(a);
console.log(z);
// post decremento
z= b--;
console.log(b);
console.log(z);

